<template>
  <div class="contador">
    <button @click="subtrair">-</button>
    <span>{{ contador }}</span>
    <button @click="adicionar">+</button>
  </div>

</template>

<script>
  export default {
    name: "MeuContador",
    data() {
      return {
        contador: 0

      }
    },
    methods: {
      adicionar() {
        this.contador++
      },
      subtrair() {
        this.contador--
      }
    }
  }
</script>

